    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        int index = searchLists(attendanceList, searchField.getText());
        
        if (index >= 0) {
            result.setText("Found list "  + " at position " + (index + 1));
            list.setSelectedIndex(index);
        } else {
            result.setText("Found 0 results. Perhaps check spelling?");
        }
    }//GEN-LAST:event_searchButtonActionPerformed

    private void addListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addListActionPerformed
        int[] indices = membersList.getSelectedIndices();
        
        if (indices.length == 0) {
            showDialog(this, "Select something!", "Error");
            return;
        }
        
        Date date;
        try {
           date = new Date(Integer.parseInt(month.getText()), Integer.parseInt(day.getText()), Integer.parseInt(year.getText()));            
        } catch (DateTimeException e) {
            showDialog(this, "Invalid Date!", "Error");
            return;
        }
        
        ArrayList<Member> attendance = new ArrayList<>();
        for (int i = 0; i < indices.length; i++) {
            attendance.add((Member) memberList.get(indices[i]));
        }
        Attendance member = new Attendance(attendance, date);
        
        int index = searchLists(attendanceList, date.toString());
        if (index >= 0) {
            showDialog(this, "Duplicate!", "Error");
            return;            
        }
        attendanceList.add(member);

        refreshList();
    }//GEN-LAST:event_addListActionPerformed

    private void editListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editListActionPerformed
        if (list.getSelectedIndex() == -1) {
            showDialog(this, "Select a List!", "Error");
            return;
        }

        if (year.getText().trim().equals("") || month.getText().trim().equals("") || day.getText().trim().equals("")) {
            showDialog(this, "One or more fields is left blank!", "Error");
            return;
        }
        
        int[] indices = membersList.getSelectedIndices();
        Date date;
        try {
           date = new Date(Integer.parseInt(month.getText()), Integer.parseInt(day.getText()), Integer.parseInt(year.getText()));            
        } catch (DateTimeException e) {
            showDialog(this, "Invalid Date!", "Error");
            return;
        }
        
        ArrayList<Member> attendancesList = new ArrayList<>();
        for (int i = 0; i < indices.length; i++) {
            attendancesList.add((Member) memberList.get(indices[i]));
        }
        Attendance attendance = attendanceList.get(list.getSelectedIndex());
        attendance.setMembers(attendancesList);
        attendance.setDate(date);

        refreshList();
    }//GEN-LAST:event_editListActionPerformed

