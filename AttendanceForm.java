    public void refreshList() {
        DefaultListModel model = new DefaultListModel();
        for (int i = 0; i < attendanceList.size(); i++) {
            model.addElement((i + 1) + ". " + attendanceList.get(i).toString());
        }    
        list.setModel(model);        
    }
    

