    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        int index;
        if (firstName.isSelected()) {
            index = search(studentList, searchField.getText(), "First Name");
        } else if (lastName.isSelected()) {
            index = search(studentList, searchField.getText(), "Last Name");
        } else if (studentNumber.isSelected()) {
            if (searchField.getText().length() != 6) {
                result.setText("Student number size should be 6");
                return;
            }
            index = search(studentList, searchField.getText(), "Student Number");
        } else {
            index = search(studentList, searchField.getText(), "First Name");
        }
        
        
        if (index >= 0) {
            result.setText("Found " + studentList.get(index).getFirstName() + " " + studentList.get(index).getLastName() + " at position " + (index + 1));
            list.setSelectedIndex(index);
            updateActionList("Search: " + (studentList.get(index).getFirstName() + " " + studentList.get(index).getLastName() + " at position " + (index + 1)));
        } else {
            result.setText("Found 0 results. Perhaps check spelling?"); 
            updateActionList("Search: 0 results.");
        }
          
    }//GEN-LAST:event_searchButtonActionPerformed

