    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        if (studentList.size() == 0) {
            showDialog(this, "There are no students!", "Error");
            return;
        }
        
        int studentNumber;
        
        try {
            studentNumber = Integer.parseInt(studentNumberField.getText());
            if (studentNumberField.getText().length() != 6) {
                showDialog(this, "Enter a valid student number!", "Error");
                return;                
            }
        } catch (NumberFormatException e) {
            showDialog(this, "Enter a valid student number!", "Error");
            return;
        }
        
        int index = search(studentList, "" + studentNumber, "Student Number");
        if (index < 0) {
            showDialog(this, "Student not found!", "Error");
            return;
        }
            
        int n = showConfirmation(this, "Are you sure you want to remove the student?", "Confirmation");
        
        if (n == 1) {
            // Cancel the removal
            return;
        }
        
        studentList.remove(index);
        firstNameField.setText("");
        lastNameField.setText("");
        grade.setSelectedIndex(0);
        studentNumberField.setText("");
        list.clearSelection();
        
        refreshList();
        updateActionList("Remove Student #" + studentNumber);       
    }//GEN-LAST:event_removeButtonActionPerformed

