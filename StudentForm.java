    private void addStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addStudentActionPerformed
        String firstName = firstNameField.getText().trim();
        String lastName = lastNameField.getText().trim();
        String choice = grade.getSelectedItem().toString();
        int studentNumber;
        
        try {
            studentNumber = Integer.parseInt(studentNumberField.getText());
            
            if (studentNumberField.getText().length() != 6) {
                showDialog(this, "Enter a valid student number!", "Error");
                return;                
            }
        } catch (NumberFormatException e) {
            showDialog(this, "Enter a valid student number!", "Error");
            return;
        }
        
        if (firstName.equals("") || lastName.equals("") || choice.equals("Choose Grade")) {
            showDialog(this, "One or more fields is left blank!", "Error");
            return;
        }
        
        for (int i = 0; i < studentList.size(); i++) {
            int index = search(studentList, "" + studentNumber, "Student Number");
            if (index >= 0) {
                showDialog(this, "There exists a student with this student number at position " + (index + 1), "Error");
                return;
            }
        }
            
        Student student = new Student(firstName, lastName, Integer.parseInt(choice), studentNumber);
        studentList.add(student);
        refreshList();
        updateActionList("Add Student: " + student);
    }//GEN-LAST:event_addStudentActionPerformed

    private void editStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editStudentActionPerformed
        if (list.getSelectedIndex() == -1) {
            showDialog(this, "Select a Student!", "Error");
            return;
        }
        
        String firstName = firstNameField.getText().trim();
        String lastName = lastNameField.getText().trim();
        String choice = grade.getSelectedItem().toString();
        int studentNumber;
        
        try {
            studentNumber = Integer.parseInt(studentNumberField.getText());
            
            if (studentNumberField.getText().length() != 6) {
                showDialog(this, "Enter a valid student number!", "Error");
                return;                
            }
        } catch (NumberFormatException e) {
            showDialog(this, "Enter a valid student number!", "Error");
            return;
        }
        
        if (firstName.equals("") || lastName.equals("") || choice.equals("Choose Grade")) {
            showDialog(this, "One or more fields is left blank!", "Error");
            return;
        }
        
        if (studentNumber != studentList.get(list.getSelectedIndex()).getStudentNumber()) {
            showDialog(this, "You can't change the student number!", "Error");
            return;        
        }
        
        Student student = studentList.get(list.getSelectedIndex());
        student.setFirstName(firstName);
        student.setLastName(lastName);
        student.setGrade(Integer.parseInt(choice));
        
        refreshList();
        updateActionList("Edit Student: " + student);           
    }//GEN-LAST:event_editStudentActionPerformed

