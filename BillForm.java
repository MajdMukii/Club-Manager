    private void removeBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBillActionPerformed
        if (billList.size() == 0) {
            showDialog(this, "There are no bills!", "Error");
            return;
        }

        int indexIssuer = search(billList, billIssuer.getText().trim(), 0);
        int indexAmount = search(billList, billAmount.getText().trim(), 1);
        if (indexIssuer < 0 || indexAmount < 0) {
            showDialog(this, "Bill not found!", "Error");
            return;
        }

        int n = showConfirmation(this, "Are you sure you want to remove the bill?", "Confirmation");
        if (n == 1) {
            // Cancel the removal
            return;
        }
        
        billList.remove(indexIssuer);
        issuer.setText("");
        amount.setText("");
        list.clearSelection();
        refreshList();
        updateActionList("Remove Bill Amount of " + billAmount.getText().trim());
    }//GEN-LAST:event_removeBillActionPerformed

