    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        int index = -1;
        if (billAmount.isSelected()) {
            index = search(billList, searchField.getText(), 1);
        } else if (billIssuer.isSelected()) {
            index = search(billList, searchField.getText(), 0);
        } 
        
        if (index >= 0) {
            result.setText("Found " + billList.get(index).toString() + " at position " + (index + 1));
            list.setSelectedIndex(index);
            updateActionList("Search: " + (billList.get(index).toString() + " at position " + (index + 1)));
        } else {
            result.setText("Found 0 results. Perhaps check spelling?");
            updateActionList("Search: 0 results");
        }
    }//GEN-LAST:event_searchButtonActionPerformed

