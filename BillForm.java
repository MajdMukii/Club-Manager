    private void addBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBillActionPerformed
        String billIssuer = issuer.getText();
        
        double billAmount;
        try {
            billAmount = Double.parseDouble(amount.getText());
            
            if (billAmount < 0) {
                showDialog(this, "Enter a valid bill amount!", "Error");
                return;            
            }
        } catch (NumberFormatException e) {
            showDialog(this, "Enter a valid bill amount!", "Error");
            return;
        }

        if (billIssuer.trim().equals("")) {
            showDialog(this, "Enter an issuer!", "Error");
            return;
        }

        Bill bill;
        if (amount.getText().trim().equals("")) {
            bill = new Bill(billIssuer);
        } else {
            bill = new Bill(billAmount, billIssuer);
        }
        
        billList.add(bill);
        refreshList();
        updateActionList("Add Bill:" + bill);
    }//GEN-LAST:event_addBillActionPerformed

    private void editBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBillActionPerformed
        if (list.getSelectedIndex() == -1) {
            showDialog(this, "Select a Bill!", "Error");
            return;
        }

        String billIssuer = issuer.getText().trim();
        double billAmount;

        try {
            billAmount = Double.parseDouble(amount.getText());
            
            if (billAmount < 0) {
                showDialog(this, "Enter a valid bill amount!", "Error");
                return;            
            }
        } catch (NumberFormatException e) {
            showDialog(this, "Enter a valid bill amount!", "Error");
            return;
        }

        if (billIssuer.equals("")) {
            showDialog(this, "One or more fields is left blank!", "Error");
            return;
        }


        Bill bill = billList.get(list.getSelectedIndex());
        bill.setBillIssuer(billIssuer);
        bill.setPrice(billAmount);

        refreshList();
        updateActionList("Edit Bill: " + bill);
    }//GEN-LAST:event_editBillActionPerformed

